package SavePartyShopManager;

public class SaveShapeMana {
int giaShape , idShape ;
String tenShape ;
public SaveShapeMana() {
	
}
public SaveShapeMana(int giaShape, int idShape, String tenShape) {
	super();
	this.giaShape = giaShape;
	this.idShape = idShape;
	this.tenShape = tenShape;
}
public int getGiaShape() {
	return giaShape;
}
public void setGiaShape(int giaShape) {
	this.giaShape = giaShape;
}
public int getIdShape() {
	return idShape;
}
public void setIdShape(int idShape) {
	this.idShape = idShape;
}
public String getTenShape() {
	return tenShape;
}
public void setTenShape(String tenShape) {
	this.tenShape = tenShape;
}


}
