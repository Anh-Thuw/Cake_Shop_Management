package SavePartyShopManager;

public class SaveColorMana {
int giaColor , idColor;
String tenColor;
public SaveColorMana() {
	
}
public SaveColorMana(int giaColor, int idColor, String tenColor) {
	super();
	this.giaColor = giaColor;
	this.idColor = idColor;
	this.tenColor = tenColor;
}
public int getGiaColor() {
	return giaColor;
}
public void setGiaColor(int giaColor) {
	this.giaColor = giaColor;
}
public int getIdColor() {
	return idColor;
}
public void setIdColor(int idColor) {
	this.idColor = idColor;
}
public String getTenColor() {
	return tenColor;
}
public void setTenColor(String tenColor) {
	this.tenColor = tenColor;
}

}
