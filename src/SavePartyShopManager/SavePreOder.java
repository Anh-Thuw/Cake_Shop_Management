package SavePartyShopManager;

public class SavePreOder {
String shape , color , tier , fruit , part , taste , timeget , requirement ;
double tong ;
public SavePreOder(String shape, String color, String tier, String fruit, String part, String taste, String timeget,
		String requirement, double tong) {
	super();
	this.shape = shape;
	this.color = color;
	this.tier = tier;
	this.fruit = fruit;
	this.part = part;
	this.taste = taste;
	this.timeget = timeget;
	this.requirement = requirement;
	this.tong = tong;
}
public String getShape() {
	return shape;
}
public void setShape(String shape) {
	this.shape = shape;
}
public String getColor() {
	return color;
}
public void setColor(String color) {
	this.color = color;
}
public String getTier() {
	return tier;
}
public void setTier(String tier) {
	this.tier = tier;
}
public String getFruit() {
	return fruit;
}
public void setFruit(String fruit) {
	this.fruit = fruit;
}
public String getPart() {
	return part;
}
public void setPart(String part) {
	this.part = part;
}
public String getTaste() {
	return taste;
}
public void setTaste(String taste) {
	this.taste = taste;
}
public String getTimeget() {
	return timeget;
}
public void setTimeget(String timeget) {
	this.timeget = timeget;
}
public String getRequirement() {
	return requirement;
}
public void setRequirement(String requirement) {
	this.requirement = requirement;
}
public double getTong() {
	return tong;
}
public void setTong(double tong) {
	this.tong = tong;
}


}
